que=input("Question:")
if que=="What is meant by machinery?":
    print("Machinery refers to a group of machines or mechanical devices used to perform work by transmitting or modifying force and motion.")

elif que=="What are the main components of a machine?":
    print("The main components are frame, power source, transmission system, control system, and working parts.")

elif que=="What is the difference between a machine and a mechanism?":
    print("A mechanism transmits motion only, while a machine transmits motion and power to perform useful work.")

elif que=="What are prime movers? Give examples":
    print("Prime movers are machines that convert natural energy into mechanical energy.")
    
elif que=="What is the function of gears in machinery?":
    print("Gears are used to transmit power, change speed, torque, and direction of motion.")

elif que=="Explain the working principle of a lathe machine.":
    print("In a lathe machine, the workpiece rotates while the cutting tool remains stationary to remove material and shape the object.")

elif que=="What are the different types of machines used in industries?":
    print("Common types include lathe machines, drilling machines, milling machines, grinding machines, and CNC machines.")

elif que=="What is preventive maintenance of machinery?":
    print("Preventive maintenance is regular inspection, cleaning, and servicing of machines to prevent breakdowns.")

elif que=="Why is lubrication important for machines?":
    print("Lubrication reduces friction, wear, heat generation, and increases machine life.")

elif que=="What are the common causes of machinery failure?":
    print("Lack of maintenance, improper lubrication, overloading, poor operation, and wear and tear.")     

elif que=="Explain the term “machine efficiency”.":
    print("Machine efficiency is the ratio of useful output power to input power, usually expressed as a percentage.")

elif que=="What safety precautions should be followed while operating machinery?":      
    print("Wear safety gear, follow instructions, avoid loose clothing, keep guards in place, and never overload machines.")    

elif que=="What is automation in machinery?":
    print("Automation is the use of control systems and technology to operate machinery with minimal human intervention.")

elif que=="What are CNC machines? Mention their advantages.":
    print("CNC (Computer Numerical Control) machines are automated machines controlled by computers.\nAdvantages: high accuracy, fast production, reduced human error, and repeatability.")

elif que=="What is the role of bearings in machinery?":
    print("Bearings support rotating parts, reduce friction, and allow smooth movement.")

